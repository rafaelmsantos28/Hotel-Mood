        Proprietario prop1 = new Proprietario(true, 100, "Rebeca", 39);
        Proprietario prop2 = new Proprietario(false, 50, "Linda", 57);
        Proprietario prop3 = new Proprietario(true, 20, "Romario", 24);
        Proprietario prop4 = new Proprietario(true, 20, "Fabricio", 24);
        Proprietario prop5 = new Proprietario(true, 20, "Orlando", 24);

        Hotel h1 = new Hotel("Pegasus", "Rua 1", prop1, s1);
        Hotel h2 = new Hotel("Capricornio", "Rua 2", prop2, s2);
        Hotel h3 = new Hotel("Aquarius", "Rua 3", prop3, s3);
        Hotel h4 = new Hotel("Bosque dos Eucaliptos", "Rua 4", prop4, s4);
        Hotel h5 = new Hotel("Campo dos Alemaes", "Rua 5", prop5, s5);

        HotelRepositorio hrepositorio = new HotelRepositorio();
        hrepositorio.adicionar(h1);
        hrepositorio.adicionar(h2);
        hrepositorio.adicionar(h3);
        hrepositorio.adicionar(h4);
        hrepositorio.adicionar(h5);

        JOptionPane.showMessageDialog(this, "Dados dos hoteis carregado com sucesso!", "Menssagem", JOptionPane.INFORMATION_MESSAGE);

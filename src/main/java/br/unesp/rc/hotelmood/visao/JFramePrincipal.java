            Avaliacao a2 = new Avaliacao(p2, h2, c2);
            Avaliacao a3 = new Avaliacao(p3, h3, c3);
            Avaliacao a4 = new Avaliacao(p4, h4, c4);
            Avaliacao a5 = new Avaliacao(p5, h5, c5);
            
            AvaliacaoRepositorio arepositorio = new AvaliacaoRepositorio();

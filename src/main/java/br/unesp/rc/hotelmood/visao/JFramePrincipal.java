            a1.setSentimento(analisador.analisar(c1.getMenssagem()));
            a2.setSentimento(analisador.analisar(c2.getMenssagem()));
            a3.setSentimento(analisador.analisar(c3.getMenssagem()));
            a4.setSentimento(analisador.analisar(c4.getMenssagem()));
            a5.setSentimento(analisador.analisar(c5.getMenssagem()));
            
            GeradorDrools drools = new GeradorDrools("rules/Sample.drl");
            drools.configurar();
            drools.adicionarObjeto(a1);
            drools.adicionarObjeto(a2);
            drools.adicionarObjeto(a3);
            drools.adicionarObjeto(a4);
            drools.adicionarObjeto(a5);
            drools.executarRegra();
            
            JOptionPane.showMessageDialog(this, "Dados das avaliacoes carregado com sucesso!", "Menssagem", JOptionPane.INFORMATION_MESSAGE);
